import math  #importando o módulo math

print(math.sqrt(81)) #imprimindo a raiz quadrada de 81

print(math.pi) #imprimindo o valor de pi 

print(math.factorial(5)) #imprimindo o fatorial de 5

print(math.pow(2, 3)) #imprimindo 2 elevado a 3

print(math.sin(math.pi/2)) #imprimindo o seno de pi/2

print(math.cos(math.pi)) #imprimindo o cosseno de pi

print(math.tan(math.pi/4)) #imprimindo a tangente de pi/4

print(math.log(100, 10)) #imprimindo o logaritmo de 100 na base 10

print(math.log(100)) #imprimindo o logaritmo natural de 100

print(math.degrees(math.pi)) #imprimindo pi em graus

print(math.radians(180)) #imprimindo 180 graus em radianos

print(math.ceil(4.2)) #imprimindo o teto de 4.2

print(math.floor(4.8)) #imprimindo o piso de 4.8


